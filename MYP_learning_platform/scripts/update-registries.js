#!/usr/bin/env node
/**
 * MYP Exam Platform — Registry Auto-Updater
 *
 * Scans data/papers/ and regenerates all registry/routing files so that
 * adding a new paper = drop the folder + run this script. Zero manual edits.
 *
 * Files regenerated:
 *   1. data/markschemes/registry.ts   — getMSForPaper()
 *   2. data/concepts/registry.ts      — getConceptEntry()
 *   3. app/exam/[paperId]/page.tsx    — PAPERS routing map
 *
 * Usage:
 *   node scripts/update-registries.js
 */

const fs   = require('fs')
const path = require('path')

// ─── Paths & config ───────────────────────────────────────────────────────────

const ROOT        = path.join(__dirname, '..')
const PAPERS_DIR  = path.join(ROOT, 'data/papers')

// The canonical paper: provides type definitions and is the legacy fallback.
// Must have markscheme.ts and conceptsPublic.ts.
const CANONICAL_PAPER = 'physics-nov-2023'
const MS_REGISTRY = path.join(ROOT, 'data/markschemes/registry.ts')
const CP_REGISTRY = path.join(ROOT, 'data/concepts/registry.ts')
const EXAM_PAGE    = path.join(ROOT, 'app/exam/[paperId]/page.tsx')
const PAPERS_PAGE  = path.join(ROOT, 'app/(platform)/papers/page.tsx')

// ─── Discover papers ─────────────────────────────────────────────────────────

/**
 * Returns all paper IDs that have the given file (e.g. 'markscheme.ts').
 * Skips directories starting with '_'.
 */
function papersWithFile(filename) {
  return fs
    .readdirSync(PAPERS_DIR, { withFileTypes: true })
    .filter(e => e.isDirectory() && !e.name.startsWith('_'))
    .map(e => e.name)
    .filter(id => fs.existsSync(path.join(PAPERS_DIR, id, filename)))
}

/**
 * Detect the exported `CONCEPTS_*` constant name from a conceptsPublic.ts file.
 * Falls back to 'CONCEPTS_PUBLIC' if not found.
 */
function detectConceptsExport(paperId) {
  const file = path.join(PAPERS_DIR, paperId, 'conceptsPublic.ts')
  const src  = fs.readFileSync(file, 'utf8')
  const m    = src.match(/export\s+const\s+(CONCEPTS_\w+)/)
  return m ? m[1] : 'CONCEPTS_PUBLIC'
}

/**
 * Convert a paper ID (e.g. 'physics-nov-2024') to a safe TS identifier
 * (e.g. 'physics_nov_2024').
 */
function safeId(paperId) {
  return paperId.replace(/-/g, '_')
}

// ─── Generator 1: data/markschemes/registry.ts ───────────────────────────────

function generateMSRegistry(paperIds) {
  // Canonical paper provides the MSRecord type and acts as legacy fallback.
  // Move it to front of list so it appears first in imports.
  const sorted   = [CANONICAL_PAPER, ...paperIds.filter(id => id !== CANONICAL_PAPER)]
  const canonical = CANONICAL_PAPER

  const importLines = sorted.map(
    id => `import { MS as MS_${safeId(id)} } from '@/data/papers/${id}/markscheme'`
  ).join('\n')

  const registryEntries = sorted.map(
    id => `  '${id}': MS_${safeId(id)},`
  ).join('\n')

  return `import 'server-only'
/**
 * AUTO-GENERATED by scripts/update-registries.js — do not edit manually.
 * Run: node scripts/update-registries.js
 *
 * Maps paperId → MS record so the marking engine can grade any paper.
 */
import type { MSRecord } from '@/data/papers/${canonical}/markscheme'
export type { MSRecord }

${importLines}

const _registry: Record<string, Record<string, MSRecord>> = {
${registryEntries}
  // Add new papers by running: node scripts/update-registries.js
}

/**
 * Get the mark scheme record for a given paper.
 * Falls back to ${canonical} if paperId not found (legacy behaviour).
 */
export function getMSForPaper(paperId: string): Record<string, MSRecord> {
  return _registry[paperId] ?? MS_${safeId(canonical)}
}
`
}

// ─── Generator 2: data/concepts/registry.ts ──────────────────────────────────

function generateConceptsRegistry(paperIds) {
  const canonical = CANONICAL_PAPER
  const sorted    = [canonical, ...paperIds.filter(id => id !== canonical)]
  const canonicalExport = detectConceptsExport(canonical)

  const importLines = sorted.map(id => {
    const exportName = detectConceptsExport(id)
    const alias      = `CONCEPTS_${safeId(id)}`
    // If the export name already matches what we'd call it, no alias needed;
    // but using 'as alias' is always safe.
    return `import { ${exportName} as ${alias} } from '@/data/papers/${id}/conceptsPublic'`
  }).join('\n')

  const registryEntries = sorted.map(
    id => `  '${id}': CONCEPTS_${safeId(id)},`
  ).join('\n')

  return `/**
 * AUTO-GENERATED by scripts/update-registries.js — do not edit manually.
 * Run: node scripts/update-registries.js
 *
 * Global concepts registry — merges conceptsPublic from every paper.
 * LiveKeywordTracker imports from here so it works across all papers.
 *
 * Keys are NOT globally unique — they're namespaced by paperId:
 *   \`{paperId}::q{id}_{label}\`  e.g. 'physics-nov-2023::q3_a'
 *
 * For backwards compat the ${canonical} keys are also exported bare.
 */
import type { ConceptEntry } from '@/data/papers/${canonical}/conceptsPublic'
export type { ConceptEntry }

${importLines}

// Build the merged registry
const _registry: Record<string, Record<string, ConceptEntry>> = {
${registryEntries}
}

/**
 * Look up a concept entry for a given paper and question/task key.
 * @param paperId  e.g. 'physics-may-2025'
 * @param key      e.g. 'q6_g'  (format: q{id}_{label})
 */
export function getConceptEntry(paperId: string, key: string): ConceptEntry | undefined {
  return _registry[paperId]?.[key]
}

// Backwards compat — re-export the canonical paper's concepts under the original name
export { CONCEPTS_${safeId(canonical)} as CONCEPTS_PUBLIC }
`
}

// ─── Generator 3: app/exam/[paperId]/page.tsx ─────────────────────────────────

function generateExamPage(paperIds) {
  // Keep canonical first for readability
  const sorted = [CANONICAL_PAPER, ...paperIds.filter(id => id !== CANONICAL_PAPER)]
  const importLines = sorted.map(id => {
    const alias = safeId(id)
    return `import { questions as ${alias}Questions, paperMeta as ${alias}Meta } from '@/data/papers/${id}/questions'`
  }).join('\n')

  const papersEntries = sorted.map(id => {
    const alias = safeId(id)
    return `  '${id}': { questions: ${alias}Questions, meta: ${alias}Meta },`
  }).join('\n')

  return `// AUTO-GENERATED by scripts/update-registries.js — do not edit manually.
// Run: node scripts/update-registries.js
import { notFound } from 'next/navigation'
import ExamShell from '@/components/exam/ExamShell'
${importLines}
import type { Question } from '@/lib/types'

interface PageProps {
  params: { paperId: string }
}

interface PaperMeta {
  id: string
  subject: string
  session: string
  year: number
  totalMarks: number
  durationMinutes: number
  criteria: readonly string[]
}

const PAPERS: Record<string, { questions: Question[]; meta: PaperMeta }> = {
${papersEntries}
}

export default function ExamPage({ params }: PageProps) {
  const { paperId } = params
  const paper = PAPERS[paperId]

  if (!paper) {
    notFound()
  }

  return <ExamShell questions={paper.questions} paperId={paperId} />
}
`
}

// ─── Helpers: read paperMeta values from questions.ts ────────────────────────

/**
 * Parse totalMarks and durationMinutes from a paper's questions.ts file.
 * Uses simple regex — no need for a full TS parser.
 */
function parsePaperMeta(paperId) {
  const file = path.join(PAPERS_DIR, paperId, 'questions.ts')
  const src  = fs.readFileSync(file, 'utf8')
  const marks  = src.match(/totalMarks\s*:\s*(\d+)/)
  const mins   = src.match(/durationMinutes\s*:\s*(\d+)/)
  return {
    total_marks:       marks ? parseInt(marks[1], 10) : 100,
    duration_minutes:  mins  ? parseInt(mins[1], 10)  : 90,
  }
}

// ─── Updater 4: LOCAL_PAPER_META in app/(platform)/papers/page.tsx ────────────

/**
 * Surgically replace the LOCAL_PAPER_META block in papers/page.tsx.
 * The block is bounded by "// Override Supabase values" and the closing
 * curly brace of the object literal, so we can reliably find + replace it.
 */
function updatePapersPage(questionsIds) {
  const src = fs.readFileSync(PAPERS_PAGE, 'utf8')

  // Build new entries
  const entries = questionsIds.map(id => {
    const { total_marks, duration_minutes } = parsePaperMeta(id)
    return `    '${id}': { total_marks: ${total_marks}, duration_minutes: ${duration_minutes} },`
  }).join('\n')

  const newBlock = `// Override Supabase values with local paper metadata (source of truth)
  // AUTO-GENERATED by scripts/update-registries.js — do not edit manually.
  const LOCAL_PAPER_META: Record<string, Partial<Paper>> = {
${entries}
  }`

  // Replace everything from the comment to the closing brace of the block
  // Match from the "Override Supabase" comment to the closing brace of LOCAL_PAPER_META
  // Anchored to the const declaration to avoid matching nested braces
  const blockRe = /(\/\/ Override Supabase values[^\n]*\n)([\s\S]*?)(const LOCAL_PAPER_META[\s\S]*?\n\s*\})/

  if (!blockRe.test(src)) {
    console.log('⚠  papers/page.tsx: LOCAL_PAPER_META block not found — skipping')
    return
  }

  const updated = src.replace(blockRe, newBlock)

  fs.writeFileSync(PAPERS_PAGE, updated)
  console.log(`✅ Patched: app/(platform)/papers/page.tsx LOCAL_PAPER_META (${questionsIds.length} papers)`)
}

// ─── Main ─────────────────────────────────────────────────────────────────────

function main() {
  console.log('\n=== MYP Registry Auto-Updater ===\n')

  // Discover
  const msIds       = papersWithFile('markscheme.ts')
  const conceptsIds = papersWithFile('conceptsPublic.ts')
  const questionsIds = papersWithFile('questions.ts')

  console.log(`Found ${questionsIds.length} papers with questions.ts:  ${questionsIds.join(', ')}`)
  console.log(`Found ${msIds.length} papers with markscheme.ts:        ${msIds.join(', ')}`)
  console.log(`Found ${conceptsIds.length} papers with conceptsPublic.ts: ${conceptsIds.join(', ')}\n`)

  if (msIds.length === 0) {
    console.error('ERROR: No papers with markscheme.ts found. Aborting.')
    process.exit(1)
  }

  // Generate markscheme registry
  const msContent = generateMSRegistry(msIds)
  fs.writeFileSync(MS_REGISTRY, msContent)
  console.log(`✅ Written: data/markschemes/registry.ts (${msIds.length} papers)`)

  // Generate concepts registry
  if (conceptsIds.length > 0) {
    const cpContent = generateConceptsRegistry(conceptsIds)
    fs.writeFileSync(CP_REGISTRY, cpContent)
    console.log(`✅ Written: data/concepts/registry.ts (${conceptsIds.length} papers)`)
  } else {
    console.log('⚠  Skipped: data/concepts/registry.ts — no papers with conceptsPublic.ts')
  }

  // Generate exam page
  if (questionsIds.length > 0) {
    const examContent = generateExamPage(questionsIds)
    fs.writeFileSync(EXAM_PAGE, examContent)
    console.log(`✅ Written: app/exam/[paperId]/page.tsx (${questionsIds.length} papers)`)
  }

  // Update papers/page.tsx LOCAL_PAPER_META
  updatePapersPage(questionsIds)

  console.log('\n=== Done. Run: npx tsc --noEmit to verify ===\n')
}

main()
